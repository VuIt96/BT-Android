package com.example.bt.GridView;

public class Images  {
    private String name;
    private int imgs;

    public Images(String name, int imgs) {
        this.name = name;
        this.imgs = imgs;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImgs() {
        return imgs;
    }

    public void setImgs(int imgs) {
        this.imgs = imgs;
    }
}
