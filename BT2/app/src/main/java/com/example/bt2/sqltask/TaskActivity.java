package com.example.bt2.sqltask;

public class TaskActivity {
    private int Id;
    private String Name;

    public TaskActivity(int id, String name) {
        Id = id;
        Name = name;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
